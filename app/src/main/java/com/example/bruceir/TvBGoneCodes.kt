object TvBGoneCodes {

    data class RawCode(
        val name: String,
        val timerVal: Int,
        val numPairs: Int,
        val bitCompression: Int,
        val times: IntArray,
        val codes: ByteArray
    )

    private fun b(vararg bytes: Int) = ByteArray(bytes.size) { bytes[it].toByte() }
    private fun i(vararg ints: Int) = intArrayOf(*ints)

    private fun decompress(raw: RawCode): IrCommand {
        val frequency = if (raw.timerVal == 0) 38000 else raw.timerVal * 1000
        val periodMicroseconds = 1000000.0 / frequency
        
        val resultPattern = mutableListOf<Int>()
        var codeIndex = 0
        var bitOffset = 0
        
        repeat(raw.numPairs) {
            var pulseIdx = 0
            repeat(raw.bitCompression) {
                val bit = (raw.codes[codeIndex].toInt() shr (7 - bitOffset)) and 1
                pulseIdx = (pulseIdx shl 1) or bit
                bitOffset++
                if (bitOffset == 8) { bitOffset = 0; codeIndex++ }
            }
            
            var spaceIdx = 0
            repeat(raw.bitCompression) {
                val bit = (raw.codes[codeIndex].toInt() shr (7 - bitOffset)) and 1
                spaceIdx = (spaceIdx shl 1) or bit
                bitOffset++
                if (bitOffset == 8) { bitOffset = 0; codeIndex++ }
            }
            
            resultPattern.add((raw.times[pulseIdx] * periodMicroseconds).toInt())
            resultPattern.add((raw.times[spaceIdx] * periodMicroseconds).toInt())
        }
        
        return IrCommand(raw.name, frequency, resultPattern.toIntArray())
    }

    fun getNaCodes(): List<IrCommand> = naRaw.map { decompress(it) }
    fun getEuCodes(): List<IrCommand> = euRaw.map { decompress(it) }

